public class typeconversion{
    public static void main(String[]args){
        int num=10;
        long num1=num;
        float num2=num1;
        double num3= num1;

        System.out.println(num+"int");
        System.out.println(num1+"long");
        System.out.println(num2+"float");
        System.out.println(num3+"double");


    }
}