public class Bitwiseoperator{
    public static void main(String[]args){

        int a=5;
        int b=3;

        System.out.println("bitwise AND(&)  -->  (a&b) :"+ (a&b));
        System.out.println("bitwise OR(|)  -->  (a|b) :"+ (a|b));
        System.out.println("bitwise EX-OR(^)  -->  (a^b) :" + (a^b));
        System.out.println("bitwise NOT or complement(~)  -->  (~b) :" + (~b));
        System.out.println("bitwise leftshift(&)  -->  (a<<b) : "+ (a<<b));
         System.out.println("bitwise Rightshift(&)  -->  (a>>b) :"+ (a>>b));
    }
}